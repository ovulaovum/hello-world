function [f,g,G] = m(xk1,xk2)
%二维Rosenbrock函数的二阶近似
%G在y<x^2+1/200中正定
%xk1,xk2是该子问题的迭代点
f = (1-xk1).^2+100.*(xk2-xk1.^2).^2;
g1 = -2.*(1-xk1)-400.*xk1.*(xk2-xk1.^2);
g2 = 200.*(xk2-xk1.^2);
g = [g1,g2]';
G11 = 2-400.*(xk2-xk1.^2)+800.*xk1.^2;
G12 = -400.*xk1;
G22 = 200;
G=[G11 G12
    G12 G22];
end